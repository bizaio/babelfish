package io.biza.cdr.babelfish.v1.enumerations;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonValue;
import io.biza.cdr.babelfish.support.BabelFishModel;
import io.biza.cdr.babelfish.support.LabelValueEnumInterface;

@BabelFishModel(description = "Day of Week", modelName = "CommonWeekDay")
public enum CommonWeekDay implements LabelValueEnumInterface {
  // @formatter:off
  MON("MON", "Monday"),
  TUE("TUE", "Tuesday"),
  WED("WED", "Wednesday"),
  THU("THU", "Thursday"),
  FRI("FRI", "Friday"),
  SAT("SAT", "Saturday"),
  SUN("SUN", "Sunday");
  // @formatter:on

  private String value;

  private String label;

  CommonWeekDay(String value, String label) {
    this.value = value;
    this.label = label;
  }

  @Override
  @JsonValue
  public String toString() {
    return String.valueOf(value);
  }

  @JsonCreator
  public static CommonWeekDay fromValue(String text) {
    for (CommonWeekDay b : CommonWeekDay.values()) {
      if (String.valueOf(b.value).equals(text)) {
        return b;
      }
    }
    return null;
  }

  @Override
  @JsonIgnore
  public String label() {
    return label;
  }
}
